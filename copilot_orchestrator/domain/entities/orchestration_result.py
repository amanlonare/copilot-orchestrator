from collections.abc import Mapping
from dataclasses import dataclass, field
from typing import Any

from copilot_orchestrator.domain.entities.action import Action
from copilot_orchestrator.domain.entities.citation import Citation


@dataclass(slots=True)
class OrchestrationResult:
    """The final response from the orchestration system for the end user.

    Attributes:
        answer: Human-readable response generated by the agent.
        citations: List of source references that justify the answer.
        actions: Next best actions or follow-up tool calls.
        confidence: Combined score representing the engine's certainty.
        fallback: If true, indicates a generic or default response.
        metadata: Detailed trace and processing stats (usage, timing).
    """

    answer: str
    citations: list[Citation] = field(default_factory=list)
    actions: list[Action] = field(default_factory=list)
    confidence: float | None = None
    fallback: bool = False
    metadata: Mapping[str, Any] = field(default_factory=dict)
